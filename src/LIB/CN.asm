%MACRO CONVERT.TO.NUMBER 1
	PUSHA
	MOV SI,%1
	MOV CH,10
	MOV DX,0
	MOV AH,0
	CALL CONVERTN
	MOV AX,DX
	MOV DI,%1
	STOSB
%ENDMACRO

CONVERTN:

CONVERTN_LOOP:
	LODSB
	CMP AL,0
	JE CONVERTN_END
	SUB AL,48
	MOV BX,AX
	MOV AX,DX
	MUL CX
	ADD AX,BX
	MOV DX,AX
	JMP CONVERTN_LOOP
	
CONVERTN_END:
	RET
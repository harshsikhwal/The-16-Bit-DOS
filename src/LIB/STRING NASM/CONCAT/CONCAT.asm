[ORG 0X7C00]

[BITS 16]

JMP BEGIN

	STRING1 DB "HELLO",0
	STRING2 DB "WORLD",0
	STRING TIMES 100 DB 0
	
BEGIN:
		MOV SI,STRING1
		MOV DI,STRING
		
TRAVERSE1:
		LODSB
		CMP AL,0
		JE NEXT
		STOSB
		JMP TRAVERSE1
		
NEXT:	MOV SI,STRING2
		MOV AL,32
		STOSB
		
TRAVERSE2:
		LODSB
		CMP AL,0
		JE PRINT
		STOSB
		JMP TRAVERSE2
		
PRINT:	MOV SI,STRING
LOOPP:	MOV AH,0X0E
		LODSB
		CMP AL,0
		JE EXIT
		INT 0X10
		JMP LOOPP
		
EXIT:	HLT

TIMES 510-($-$$)DB 0
DW 0XAA55
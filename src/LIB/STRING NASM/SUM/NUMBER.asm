[ORG 0X7C00]

[BITS 16]

JMP BEGIN
	NUMBER1 TIMES 20 DB 0
	NUMBER2 TIMES 20 DB 0
	NUMBER TIMES 20 DB 0
	MSG1 DB "ENTER NUMBER 1:",0
	MSG2 DB 13,10,"ENTER NUMBER 2:",0
	MSG3 DB 13,10,"SUM:",0
%INCLUDE "C:/LIB/IO.asm"


%MACRO CONVERT.TO.NUMBER 1
	PUSHA
	MOV SI,%1
	MOV CX,10
	MOV DX,0
	MOV AH,0
	CALL CONVERTN
	MOV AX,DX
	MOV DI,%1
	STOSB
	MOV AL,0
	STOSB
%ENDMACRO


CONVERTN:

CONVERTN_LOOP:
	LODSB
	CMP AL,0
	JE CONVERTN_END
	SUB AL,48
	MOV BX,AX
	MOV AX,DX
	MUL CX
	ADD AX,BX
	MOV DX,AX
	JMP CONVERTN_LOOP
	
CONVERTN_END:
	RET



BEGIN:
	
	PRINT.STRING MSG1
	READ.STRING NUMBER1
	PRINT.STRING MSG2
	READ.STRING NUMBER2
	CONVERT.TO.NUMBER NUMBER1
	CONVERT.TO.NUMBER NUMBER2
	MOV SI,NUMBER1
	LODSB
	MOV AH,AL
	MOV SI,NUMBER2
	LODSB
	ADD AL,AH
	MOV DI,NUMBER
	STOSB
	MOV AL,0
	STOSB
	PRINT.STRING MSG3
	PRINT.STRING NUMBER
	HLT
	

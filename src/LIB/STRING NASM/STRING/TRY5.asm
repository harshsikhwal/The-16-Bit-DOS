;PRINT A STRING USING MACRO

[ORG 0X7C00]

[BITS 16]

JMP BEGIN

STRING DB "HELLO WORLD!",0

%MACRO PRINT.STRING 1
		MOV SI,%1
		PUSHA
		CALL PRINTS
		POPA
%ENDMACRO

PRINTS:
LOOPP:	MOV AH,0X0E
		LODSB
		CMP AL,0
		JE LOOPP_DONE
		INT 0X10
		JMP LOOPP
	
LOOPP_DONE:
		RET

BEGIN:
		PRINT.STRING STRING
		HLT
		

TIMES 510-($-$$)DB 0
DW 0XAA55
